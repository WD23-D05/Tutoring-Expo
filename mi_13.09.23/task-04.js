/*
Aufgabe 4: Funktion mit Default-Parameter
Schreiben Sie eine Funktion, die zwei Zahlen multipliziert. Wenn der zweite Parameter fehlt, soll er standardmäßig 1 sein.
*/

function multipliziere(a, b = 1) {
    return a * b;
  }




// Beispiel-Lösung 4:
const produkt1 = multipliziere(3, 4);
const produkt2 = multipliziere(5);
console.log(produkt1); // Ausgabe: 12
console.log(produkt2); // Ausgabe: 5